package day03increment_decrement_operation;

public class increment_review {

	public static void main(String[] args) {
		
		int age= 10;
		
		age= age+5;
		System.out.println(age);
		
		age+=5;
		System.out.println(age);
		
		age++;
		
		System.out.println(age);
		//////////////////////////////////
		age=age-5;
		System.out.println(age);
		
		age-=5;
		System.out.println(age);
		
		age--;
		System.out.println(age);
	
		///////////////////////////////////////
		
		age=age*2;
		System.out.println(age);
		
		age*=2;
		System.out.println(age);
		//System.out.println((age*=2));
		

	}

}
