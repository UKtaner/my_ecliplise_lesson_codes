package day03increment_decrement_operation;

public class Swap_Review {

	public static void main(String[] args) {
		
		int a=10;
		int b=21;
		System.out.println("first value of a "+a);
		System.out.println("first value of b "+b);
		int temp=0;
		
		temp =b;
		b=a;
		a=temp;
		System.out.println("swap value of a "+a);
		System.out.println("swap value of b "+b);
		
		
				
		
		
		

	}

}
